if ('SpeechRecognition' in window || 'webkitSpeechRecognition' in window) {
    // Create a new instance of the SpeechRecognition object
    const recognition = new (window.SpeechRecognition || window.webkitSpeechRecognition)();

    // Set the parameters
    recognition.lang = 'en-US'; // Language of the speech recognition
    recognition.continuous = true; // Keep listening for speech until stopped

    // Event handler for when speech recognition starts
    recognition.onstart = function() {
      console.log('Speech recognition started');
    };

    // Event handler for when speech recognition results are available
    recognition.onresult = function(event) {
      const result = event.results[event.results.length - 1][0].transcript;
      document.getElementById('searchInput').value = result;
      document.getElementById('searchButton').disabled = false;
    };

    // Event handler for errors that occur during speech recognition
    recognition.onerror = function(event) {
      console.error('Speech recognition error', event.error);
    };

    // Event handler for when speech recognition ends
    recognition.onend = function() {
      console.log('Speech recognition ended');
      document.getElementById('searchButton').disabled = true;
    };

    // Start the speech recognition when the Start button is clicked
    document.getElementById('startButton').addEventListener('click', function() {
      recognition.start();
    });
  } else {
    // The browser doesn't support speech recognition
    console.error('Speech recognition not supported');
    document.getElementById('searchInput').value = 'Speech recognition not supported';
    document.getElementById('searchButton').disabled = true;
  }